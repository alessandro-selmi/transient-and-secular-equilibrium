def eqn3(ts, nz1, nz2, nz3):
     #-time-grid-----------------------------------
     t  = np.linspace(0, tmax, 10000)                            # per opzione interpolazione si può usare questo
     #differential-eq-system----------------------
     def funct(y,t):
        Nm1=y[0]
        Nm2=y[1]
        Nm3=y[2]
        # the model equations
        f1 = -lam1*Nm1         # = dN1/dt
        f2 = -lam2*Nm2+lam1*Nm1
        f3 = -lam3*Nm3+lam2*Nm2
        return [f1, f2, f3]
     #integrate------------------------------------
