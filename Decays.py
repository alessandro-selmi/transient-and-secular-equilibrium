def model(N, t, l):

	N1, N2, N3 = N
	l1, l2, l3 = l

	dN1dt = - l1 * N1
	dN2dt = - l2 * N2 + l1 * N1
	dN3dt = - l3 * N3 + l2 * N2

	dNdt = [dN1dt, dN2dt, dN3dt]

	return dNdt


# Soluzione delle equazioni differenziali
